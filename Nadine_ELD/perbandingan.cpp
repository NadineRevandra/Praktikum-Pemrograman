#include <iostream>
int main() {
int x = 5, y = 10;
std::cout << "x == y : " << (x == y) << std::endl;
std::cout << "x != y : " << (x != y) << std::endl;
std::cout << "x > y : " << (x > y) << std::endl;
std::cout << "x < y : " << (x < y) << std::endl;
std::cout << "x >= y : " << (x >= y) << std::endl;
std::cout << "x <= y : " << (x <= y) << std::endl;
return 0;
}
